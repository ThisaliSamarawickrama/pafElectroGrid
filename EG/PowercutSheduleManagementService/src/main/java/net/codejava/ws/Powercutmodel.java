package net.codejava.ws;

public class Powercutmodel{

    
    String  district, province, grid_Area , monday, tuesday, wednesday, thursday, friday,  saturday ,sunday ;
    
    public Powercutmodel(){
        
    }

    public Powercutmodel(String district, String province, String grid_Area, String monday, String tuesday, String wednesday, String thursday, String friday, String saturday, String sunday) {
        this.district = district;
        this.province = province;
        this.grid_Area = grid_Area;
        this.monday = monday;
        this.tuesday = tuesday;
        this.wednesday = wednesday;
        this.thursday = thursday;
        this.friday = friday;
        this.saturday = saturday;
        this.sunday = sunday;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getGrid_Area() {
        return grid_Area;
    }

    public void setGrid_Area(String grid_Area) {
        this.grid_Area = grid_Area;
    }

    public String getMonday() {
        return monday;
    }

    public void setMonday(String monday) {
        this.monday = monday;
    }

    public String getTuesday() {
        return tuesday;
    }

    public void setTuesday(String tuesday) {
        this.tuesday = tuesday;
    }

    public String getWednesday() {
        return wednesday;
    }

    public void setWednesday(String wednesday) {
        this.wednesday = wednesday;
    }

    public String getThursday() {
        return thursday;
    }

    public void setThursday(String thursday) {
        this.thursday = thursday;
    }

    public String getFriday() {
        return friday;
    }

    public void setFriday(String friday) {
        this.friday = friday;
    }

    public String getSaturday() {
        return saturday;
    }

    public void setSaturday(String saturday) {
        this.saturday = saturday;
    }

    public String getSunday() {
        return sunday;
    }

    public void setSunday(String sunday) {
        this.sunday = sunday;
    }
    

}